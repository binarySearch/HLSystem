    private void setLabelName(int button){
        String labelName = "Error!";
        
        switch(button){
            case 1:
                labelName = "Registrar evento";
                break;
                
            case 2:
                labelName = "Eventos";
                break;
                
            case 3:
                labelName = "Inventario";
                break;
        
        }
        
        jLabel9.setText(labelName);
    }

